
class Employee:
    
    language = "py"  #This is class attribute
    salary = 1200000


souvik = Employee()
print(souvik.language, souvik.salary)
souvik.name = "SOUVIK BAG"  # This is object attribute
print(souvik.name,souvik.salary,souvik.language)


# Here name is object attribute and salary and language are class attributes as they directly belong to the class



















