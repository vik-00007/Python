
# Write a program to greet al the person names stored in a list and which starts with "s".

# List of names
names = ["Souvik", "Sneha", "Shreya", "Ayan", "Aparna", "Mihika"]

# Loop through each name in the list
for name in names:
    # Check if the name starts with 's'
    if name.lower().startswith('s'):
        # Print the greeting
        print(f"Hello, {name}!")

















